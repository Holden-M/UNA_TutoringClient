        }

        private void btnRegister_Click(object sender, EventArgs e)
        {
            // TODO Register User with API
            string reg_username = txtRegister_Email.Text;
            string reg_password = txtRegister_Password.Text;
            string reg_repassowrd = txtRegister_RePassword.Text;

            // REGEX with email

            if (reg_password == reg_repassowrd)
            {
                txtRegister_Password.Text = "";
